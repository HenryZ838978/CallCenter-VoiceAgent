def load_benchmark():
    with open(BENCHMARK_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)
    return {d["id"]: d for d in data["dialogues"]}


def make_single_turn(dialogue, user_text=None):
    """Generate a single-turn training sample."""
    return {
        "id": f"single_{dialogue['id']}_{hash(user_text or '') % 10000:04d}",
        "conversations": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_text or dialogue["user_input"]},
            {"role": "assistant", "content": dialogue["expected_response"]},
        ],
    }


def make_multi_turn(chain_ids, dialogues):
    """Generate a multi-turn training sample from a chain of dialogue IDs."""
    convs = [{"role": "system", "content": SYSTEM_PROMPT}]
    for did in chain_ids:
        d = dialogues[did]
        variants = USER_VARIANTS.get(did, [d["user_input"]])
        user_text = random.choice(variants)
        convs.append({"role": "user", "content": user_text})
        convs.append({"role": "assistant", "content": d["expected_response"]})

    chain_key = "_".join(chain_ids)
    return {
        "id": f"multi_{chain_key}_{random.randint(0,9999):04d}",
        "conversations": convs,
    }


def make_single_turn_rephrased(dialogue, user_text, assistant_text):
    """Generate a single-turn sample with rephrased assistant response."""
    uid = hash(f"{user_text}{assistant_text}") % 100000
    return {
        "id": f"rephrase_{dialogue['id']}_{uid:05d}",
        "conversations": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_text},
            {"role": "assistant", "content": assistant_text},
        ],
    }


def main():
    dialogues = load_benchmark()
    samples = []

    for did, d in dialogues.items():
        samples.append(make_single_turn(d))
        variants = USER_VARIANTS.get(did, [])
        for v in variants:
            if v != d["user_input"]:
                samples.append(make_single_turn(d, v))

    for did, rephrases in RESPONSE_REPHRASE.items():
        if did not in dialogues:
            continue
        d = dialogues[did]
        user_variants = USER_VARIANTS.get(did, [d["user_input"]])
        for rp in rephrases:
            for uv in user_variants:
                samples.append(make_single_turn_rephrased(d, uv, rp))

    for chain in MULTI_TURN_CHAINS:
        n_variants = 10 if len(chain) >= 5 else 6
        for _ in range(n_variants):
            samples.append(make_multi_turn(chain, dialogues))

    random.shuffle(samples)

    os.makedirs(os.path.dirname(OUTPUT_PATH), exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(samples, f, ensure_ascii=False, indent=2)

    single = sum(1 for s in samples if s["id"].startswith("single"))
    multi = sum(1 for s in samples if s["id"].startswith("multi"))
    print(f"Generated {len(samples)} samples: {single} single-turn + {multi} multi-turn")
    print(f"Saved to {OUTPUT_PATH}")

    avg_turns = sum(
        len([c for c in s["conversations"] if c["role"] == "assistant"])
        for s in samples
    ) / len(samples)
    print(f"Average assistant turns per sample: {avg_turns:.1f}")


if __name__ == "__main__":
    main()
